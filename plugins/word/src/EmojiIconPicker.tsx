export default function EmojiIconPicker({ onSelect, onClose }: EmojiIconPickerProps) {
  const [activeTab, setActiveTab] = useState(CATEGORIES[0]!.id)
  const [search, setSearch] = useState('')

  const activeCategory = CATEGORIES.find(c => c.id === activeTab) || CATEGORIES[0]!

  const filteredItems = search 
    ? CATEGORIES.flatMap(c => c.items).filter(i => i.includes(search)) // Simple search for now
    : activeCategory.items

  return (
    <div className="flex flex-col bg-white dark:bg-gray-800 shadow-2xl border border-gray-200 dark:border-gray-700 rounded-lg w-72 h-[400px] overflow-hidden animate-in fade-in zoom-in duration-150">
      {/* Search Bar */}
      <div className="flex items-center gap-2 px-3 py-2 border-b border-gray-100 dark:border-gray-700">
        <Search size={14} className="text-gray-400" />
        <input 
          autoFocus
          className="flex-1 bg-transparent text-xs outline-none text-gray-700 dark:text-gray-200" 
          placeholder="Search emojis..."
          value={search}
          onChange={(e) => setSearch(e.target.value)}
        />
        {search && (
          <button onClick={() => setSearch('')}>
            <X size={14} className="text-gray-400 hover:text-gray-600" />
          </button>
        )}
      </div>

      {/* Tabs */}
      <div className="flex items-center gap-1 px-2 py-1 bg-gray-50 dark:bg-gray-900/50 border-b border-gray-100 dark:border-gray-700 overflow-x-auto no-scrollbar">
        {CATEGORIES.map(category => {
          const IconComponent = category.icon
          return (
            <button
              key={category.id}
              title={category.name}
              onClick={() => setActiveTab(category.id)}
              className={`p-1.5 rounded transition-colors ${activeTab === category.id ? 'bg-white dark:bg-gray-700 shadow-sm text-blue-600' : 'text-gray-500 hover:bg-gray-200 dark:hover:bg-gray-800'}`}
            >
              <IconComponent size={16} />
            </button>
          )
        })}
      </div>

      {/* Items Grid */}
      <div className="flex-1 overflow-y-auto p-3 custom-scrollbar">
        <div className="text-[10px] font-bold text-gray-400 mb-2 uppercase tracking-wider">
          {search ? 'Search Results' : activeCategory.name}
        </div>
        <div className="grid grid-cols-7 gap-1">
          {filteredItems.map((item, idx) => (
            <button
              key={`${item}-${idx}`}
              onClick={() => {
                onSelect(item)
                if (!search) onClose() // Close if not in search mode for quick use
              }}
              className="h-8 w-8 flex items-center justify-center hover:bg-gray-100 dark:hover:bg-gray-700 rounded transition-all hover:scale-125 text-lg"
            >
              {item}
            </button>
          ))}
          {filteredItems.length === 0 && (
            <div className="col-span-7 py-8 text-center text-xs text-gray-500 italic">
              No results found
            </div>
          )}
        </div>
      </div>

      {/* Categories for Icons (Next Row or bottom?) */}
      <div className="p-2 border-t border-gray-100 dark:border-gray-700 bg-gray-50/50 dark:bg-gray-900/50">
          <div className="text-[9px] font-bold text-gray-400 mb-1 uppercase">Selection</div>
          <div className="flex items-center justify-between text-[11px] text-gray-400 italic">
             <span>Click to insert</span>
             <button onClick={onClose} className="text-blue-500 font-bold not-italic hover:underline">Done</button>
          </div>
      </div>
    </div>
  )
}
